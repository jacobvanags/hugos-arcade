/**
 * Level definitions for Cheese Run.
 *
 * 18 regular levels (6 per world) + 3 secret levels = 21 total.
 *
 * Tile legend:
 *   .  = air
 *   #  = solid block
 *   =  = platform (one-way, can jump through from below)
 *   ^  = spikes (instant damage)
 *   S  = player spawn
 *   G  = golden cheese (level goal)
 *   c  = small cheese pickup (+100 score)
 *   h  = heart pickup (+1 HP)
 *   1  = patrol cat spawn
 *   2  = jumping cat spawn
 *   3  = fast cat spawn
 *   4  = heavy cat spawn
 *   5  = boss cat spawn
 *   ~  = spring pad (bounce)
 *   *  = secret reward item (used in bonus rooms)
 */

// Helper: parse a level string array into tile/entity data
export function parseLevel(rows) {
  const tiles = [];
  const entities = [];
  let spawn = { x: 64, y: 64 };
  let goal = { x: 0, y: 0 };

  for (let r = 0; r < rows.length; r++) {
    const tileRow = [];
    for (let c = 0; c < rows[r].length; c++) {
      const ch = rows[r][c];
      const x = c * 32;
      const y = r * 32;

      switch (ch) {
        case '#': tileRow.push(1); break;   // solid
        case '=': tileRow.push(2); break;   // platform
        case '^': tileRow.push(3); break;   // spike
        case '~': tileRow.push(4); break;   // spring
        case 'S':
          tileRow.push(0);
          spawn = { x: x + 16, y: y + 8 };
          break;
        case 'G':
          tileRow.push(0);
          goal = { x, y };
          break;
        case 'c':
          tileRow.push(0);
          entities.push({ type: 'cheese', x: x + 16, y: y + 16 });
          break;
        case 'h':
          tileRow.push(0);
          entities.push({ type: 'heart', x: x + 16, y: y + 16 });
          break;
        case '*':
          tileRow.push(0);
          entities.push({ type: 'reward', x: x + 16, y: y + 16 });
          break;
        case '1':
          tileRow.push(0);
          entities.push({ type: 'patrol', x: x + 16, y: y + 16 });
          break;
        case '2':
          tileRow.push(0);
          entities.push({ type: 'jumper', x: x + 16, y: y + 16 });
          break;
        case '3':
          tileRow.push(0);
          entities.push({ type: 'fast', x: x + 16, y: y + 16 });
          break;
        case '4':
          tileRow.push(0);
          entities.push({ type: 'heavy', x: x + 16, y: y + 16 });
          break;
        case '5':
          tileRow.push(0);
          entities.push({ type: 'boss', x: x + 16, y: y + 16 });
          break;
        default:
          tileRow.push(0); // air
      }
    }
    tiles.push(tileRow);
  }

  return {
    tiles,
    entities,
    spawn,
    goal,
    width: rows[0].length * 32,
    height: rows.length * 32,
    cols: rows[0].length,
    rows: rows.length,
  };
}

