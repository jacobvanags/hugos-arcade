/**
 * Scale enemy HP based on wave number.
 */
export function getScaledHp(baseHp, wave) {
  return Math.floor(baseHp * (1 + wave * 0.15 + Math.pow(wave / 20, 2)));
}

/**
 * Scale enemy shield HP.
 */
export function getScaledShield(baseShield, wave) {
  return Math.floor(baseShield * (1 + wave * 0.12));
}
