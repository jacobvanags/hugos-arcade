/**
 * Get the cost to upgrade a tower on a given path.
 */
export function getUpgradeCost(towerType, pathIndex, currentTier) {
  const def = TOWER_TYPES[towerType];
  if (!def || currentTier >= 5) return Infinity;
  return def.upgradePaths[pathIndex].tiers[currentTier].cost;
}
